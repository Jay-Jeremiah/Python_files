# Casting is where a programmer specifies the data type on a variable
# int() - gives only integer number
# float() - gives numbers with decimals
# str() - constructs a string with a variety of data types

x = int(1)

y = int(2.8)  #y will be 2

z = int("3")  #z will be 3

print(x)
print(y)
print(z)

a = float(1)   # a will be 1.0
b = float(2.8) # b will be 2.8
c = float("3") # c will be 3.0


print(a)
print(b)
print(c)

p = str(2)
q = str("2.8")
r = str(" I am", 12 ,"years old")

print(p)
print(q)
print(r)

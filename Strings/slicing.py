# we can display a range of characters in a string, this is done by slicing
# there should be a start and end index

b = "Hello World!"
print(b[2:5])

# we can leave out the start index
# this gets the first characters until the 5th
# leaving out the characters beyond the 5th

x = "Jesus is Lord"
print(x[:5])

# we can also leave out the end index
# here the reange from the 2nd to the end of the string

y = "O Lord, you are worthy"
print(y[2:])


